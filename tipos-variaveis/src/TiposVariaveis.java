public class TiposVariaveis {

    public static void main(String[] args) {
        
        String caractere = "Adson Trindade"; 
        // variavel usada para declarar sequencia de caracteres.
        System.out.println(caractere);

        int numerosInteiros = 100; 
        // variavel usada para declarar numero de valor inteiro.
        System.out.println(numerosInteiros);

        double numerosFracionados = 3.14; 
        // varivel usada para declarar numero de valor decimal.
        System.out.println(numerosFracionados);

        boolean logico = true; 
        // variavel usada para declarar valor logico (verdadeiro/falso).
        System.out.println(logico);

        char caractereUnica = 'V'; 
        // variavel usada para armazenar uma unica caractere.
        System.out.println(caractereUnica); 

        final int anoNascimento = 1999; 
        // usada para armazenar valores que não se alteram.
        System.out.println(anoNascimento);

        String descrição = "Se observa acima exemplos de algumas das variaveis mais utilizadas na linguagem java e suas respectivas descrições";
        System.out.print(descrição);
    }
    
}    